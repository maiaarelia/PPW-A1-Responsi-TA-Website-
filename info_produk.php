                    <h2 class="title-post">Baby Monster, Girl Group Baru dari YG Setelah 7 Tahun BLACKPINK!</h2>
                    <div class="meta-post">
                        <span><em class="glyphicon glyphicon-user"></em>Maia Arelia</span>
                        <span><em class="glyphicon glyphicon-time"></em>13 Mei 2023</span>
                    </div>
                    <div class="content">
                        <p>YG Entertainment baru saja memperkenalkan girl group baru bernama BABYMONS7ER yang terdiri dari tujuh anggota, 
                            yaitu AHYEON, RUKA, CHIQUITA, HARAM, PHARITA, RORA, dan ASA. Awalnya, grup ini direncanakan akan debut sebagai 
                            grup beranggotakan lima orang, namun kemudian diubah menjadi tujuh orang. 
                            Grup ini dipersiapkan untuk melakukan debutnya dalam waktu dekat. 
                        </p>
                        <br>

                        <div class="card mb-3">
                            <img src="img/BAEMONN.jfif" class="card-img-top">
                          </div>
                        <br>

                        <p>
                            YANG HYUN SUK pertama kali memilih anggota Korea berusia 15 tahun <b>AHYEON,</b>, mengatakan, 
                            "Dia memiliki jangkauan vokal terluas. Saya pikir dia adalah anggota yang paling dioptimalkan sebagai artis pop. 
                            Dia memiliki kemampuan untuk memimpin BABYMONSTER." 
                        </p>
                        <p>Selanjutnya, YANG HYUN SUK memilih <b>RUKA</b>, anggota berusia 20 tahun dari Jepang. 
                            Produser memperkenalkannya sebagai 
                            "seorang teman dengan banyak pesona. Saya pikir banyak orang akan jatuh cinta pada pesona RUKA."  
                            </p>
                        <p><b>CHIQUITA</b>, anggota termuda dari Thailand, berhasil terpilih menjadi anggota ketiga BABYMONSTER dengan masa pelatihan 
                            terpendek 1 tahun 9 bulan!  Menurut Produser YANG HYUN SUK, 
                            CHIQUITA memiliki potensi perkembangan yang tinggi dan pertumbuhannya yang 
                            cepat akan menjadi kekuatannya di Industri Musik!
                            </p>
                        <p>Produser YANG HYUN SUK memperkenalkan anggota Korea berusia 15 tahun <b>HARAM</b>, 
                            mengatakan, "Melihat lagu solo yang dia nyanyikan sejauh ini, 
                            dia bernyanyi dengan gayanya sendiri, berbeda dari lagu aslinya. 
                            Faktor itu memiliki pengaruh signifikan pada seleksi. 
                            Saya pikir kemampuan kreatif dan kemampuan artistiknya akan sangat membantu BABYMONSTER."
                            </p>
                        <p>Untuk anggota terakhir, anggota Thailand berusia 17 tahun <b>PHARITA</b>, ia menambahkan, 
                            "Kelompok ini harus bertujuan untuk menjadi global. Dia berbicara bahasa Inggris 
                            dan Thailand dengan baik. Itu yang paling menggerakkan saya."
                        </p>
                        <p>Mengenai pengecualian RORA dan ASA, produser YANG HYUN SUK menjelaskan, 
                            "Aku ingin menggunakan RORA sebagai vokal utama di girl grup berikutnya 
                            yang sedang kami persiapkan karena dia masih muda. ASA telah menjadi trainee selama 
                            4 tahun 9 bulan. Saya memberikan nilai tinggi pada fakta bahwa ASA, yang dianggap hanya rap,
                            bisa bernyanyi sebagai pemain serba bisa." Dia mengungkapkan bahwa "Lee Jung memilih RUKA dan
                            ASA sebagai anggota yang menari terbaik." Dia lebih lanjut menyatakan 
                            niatnya untuk ASA, "Saya ingin pergi ke Jepang dan membuat girl grup global, 
                            dan saya ingin memasukkan ASA dalam proyek yang akan dilanjutkan dari akhir tahun ini hingga awal tahun depan."</p>
                            <br> 
                            <blockquote class="blockquote"> <p>"YANG HYUN SUK kemudian membuat pengumuman mengejutkan, 
                                "Ada begitu banyak penggemar yang menginginkan debut 7 anggota. Saya pikir penggemar YG 
                                adalah keluarga YG yang sebenarnya. Aku akan membawa ketujuh orang itu bersamaku." 
                            Dia meminta penggemar untuk memikirkan <b>RORA</b> dan <b>ASA</b> sebagai "anggota yang dipilih oleh penggemar YG," 
                            dan memprediksi, "Ini hanya perkiraan, tapi sepertinya debut tidak akan ditunda setelah musim gugur ini."
                            Akibatnya, YG secara virtual mengkonfirmasi debut grup beranggotakan tujuh orang tersebut 
                            dengan mengubah nama Inggris BABYMONSTER menjadi BABYMONS7ER."</p>
                            </blockquote> <br> 
                        <p>BABYMONSTER adalah girl grup yang diumumkan YG sekitar tujuh tahun setelah BLACKPINK. 
                            Mengingat posisi BLACKPINK sebagai grup global teratas yang memimpin K-pop, 
                            dan promosi agresif jangka panjang YG melalui perilisan video pertunjukan dan produksi reality show debut, 
                            BABYMONSTER menarik perhatian sebagai artis yang paling dinanti tahun 2023 di industri musik.</p>
                        <p>
                            BABYMONSTER, yang mengkonfirmasi debut tujuh anggotanya, 
                            terdiri dari anggota dari berbagai negara termasuk Korea (AHYEON, HARAM, RORA), 
                            Thailand (PHARITA, CHIQUITA), dan Jepang (RUKA, ASA). Di bawah pelatihan sistematis, 
                            mereka telah menguasai berbagai bahasa termasuk Korea, Inggris, Jepang, Thailand, dan Cina.
                             Pesona mereka yang beragam dan selalu berubah yang mencakup berbagai budaya, bersama dengan 
                             fakta bahwa YG telah membidik pasar global sejak awal, meningkatkan harapan untuk kegiatan dan pencapaian 
                             mereka di luar negeri.
                        </p>
